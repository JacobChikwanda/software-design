# How do we declare variables in python
name = "John"
age = 20
height = 5.6
gender = 'M'

# How do we print out information in python
print(f"Persons Information\n\nName: {name}\nAge: {age}\nHeight: {height}\nGender: {gender}")

# How do we check types in python
print(f"\nTypes of Persons Information\n\nName: {type(name)}\nAge: {type(age)}\nHeight: {type(height)}\nGender: {type(gender)}")